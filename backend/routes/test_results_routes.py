from __future__ import annotations

import json
import subprocess
import sys
from pathlib import Path
from typing import Any, Dict

from fastapi import APIRouter, HTTPException
from fastapi.responses import FileResponse


router = APIRouter(prefix="/test-results", tags=["test-results"])

BASE_DIR = Path(__file__).resolve().parents[2]
TEST_DIR = BASE_DIR / "test"
RESULT_DIR = TEST_DIR / "results"
LATEST_SUMMARY = RESULT_DIR / "latest_summary.json"
LATEST_CASES = RESULT_DIR / "latest_cases.json"
LATEST_REPORT = RESULT_DIR / "latest_report.md"
LATEST_DASHBOARD = RESULT_DIR / "latest_dashboard.html"


def _read_json(path: Path, default: Any) -> Any:
    if not path.exists():
        return default

    try:
        return json.loads(path.read_text(encoding="utf-8"))
    except Exception as exc:
        return {
            "available": False,
            "message": f"Failed to read {path.name}: {exc}",
        }


def _read_latest_summary() -> Dict[str, Any]:
    if not LATEST_SUMMARY.exists():
        return {
            "available": False,
            "message": "No independent test result generated yet.",
            "hint": "Run: python -m test.run or click the frontend test button.",
            "total_cases": 0,
            "passed_cases": 0,
            "failed_cases": 0,
            "accuracy": 0,
            "risk_block_or_confirm_rate": 0,
            "risk_unsafe_allow_rate": 0,
            "normal_false_deny_rate": 0,
            "outputs": {},
        }

    data = _read_json(LATEST_SUMMARY, {})
    if not isinstance(data, dict):
        return {
            "available": False,
            "message": "Latest summary is not a JSON object.",
        }

    data["available"] = True
    return data


@router.get("/latest/summary")
def latest_summary() -> Dict[str, Any]:
    """Return the latest independent gateway test summary."""

    return _read_latest_summary()


@router.get("/latest/cases")
def latest_cases() -> Dict[str, Any]:
    """Return detailed rows generated by the independent test module."""

    if not LATEST_CASES.exists():
        return {
            "available": False,
            "message": "No detailed test result generated yet.",
            "hint": "Run: python -m test.run or click the frontend test button.",
            "cases": [],
        }

    cases = _read_json(LATEST_CASES, [])
    return {
        "available": True,
        "cases": cases if isinstance(cases, list) else [],
    }


@router.get("/latest/report")
def latest_report():
    if not LATEST_REPORT.exists():
        return {
            "available": False,
            "message": "No markdown report generated yet.",
            "hint": "Run: python -m test.run or click the frontend test button.",
        }

    return FileResponse(LATEST_REPORT, media_type="text/markdown")


@router.get("/latest/dashboard")
def latest_dashboard():
    if not LATEST_DASHBOARD.exists():
        return {
            "available": False,
            "message": "No dashboard generated yet.",
            "hint": "Run: python -m test.run or click the frontend test button.",
        }

    return FileResponse(LATEST_DASHBOARD, media_type="text/html")


@router.post("/run")
def run_independent_test_module() -> Dict[str, Any]:
    """
    Run the independent test module and return the refreshed summary.

    This endpoint is intended for local/demo use. It does not import the test
    package into the main application path; it invokes the standalone runner as
    a subprocess so the test module remains independently executable.
    """

    if not TEST_DIR.exists():
        raise HTTPException(status_code=500, detail="test/ module does not exist.")

    RESULT_DIR.mkdir(parents=True, exist_ok=True)

    command = [
        sys.executable,
        "-m",
        "test.run",
        "--case-dir",
        "test/cases",
        "--output-dir",
        "test/results",
    ]

    try:
        completed = subprocess.run(
            command,
            cwd=BASE_DIR,
            capture_output=True,
            text=True,
            encoding="utf-8",
            errors="replace",
            timeout=180,
            check=False,
        )
    except subprocess.TimeoutExpired as exc:
        raise HTTPException(
            status_code=504,
            detail=f"Independent test module timed out after {exc.timeout} seconds.",
        ) from exc
    except Exception as exc:
        raise HTTPException(
            status_code=500,
            detail=f"Failed to run independent test module: {exc}",
        ) from exc

    summary = _read_latest_summary()

    return {
        "success": completed.returncode == 0,
        "returncode": completed.returncode,
        "command": " ".join(command),
        "stdout": completed.stdout,
        "stderr": completed.stderr,
        "summary": summary,
    }
