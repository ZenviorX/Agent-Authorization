async function runIndependentTests(): Promise<TestRunResponse> {
  const controller = new AbortController();
  const timer = window.setTimeout(() => controller.abort(), TEST_RUN_TIMEOUT_MS);

  try {
    const res = await fetch(buildUrl('/test-results/run'), {
      method: 'POST',
      headers: {
        'Content-Type': 'application/json; charset=utf-8'
      },
      signal: controller.signal
    });

    const contentType = res.headers.get('content-type') || '';
    const payload = contentType.includes('application/json')
      ? await res.json()
      : { detail: await res.text() };

    if (!res.ok) {
      const message = typeof payload?.detail === 'string'
        ? payload.detail
        : `HTTP ${res.status}`;
      throw new Error(message);
    }

    return payload as TestRunResponse;
  } finally {
    window.clearTimeout(timer);
  }
}

async function postJson(endpoint: string, body: unknown, timeoutMs = COMMAND_TIMEOUT_MS): Promise<Record<string, unknown>> {
  const controller = new AbortController();
  const timer = window.setTimeout(() => controller.abort(), timeoutMs);

  try {
    const res = await fetch(buildUrl(endpoint), {
      method: 'POST',
      headers: {
        'Content-Type': 'application/json; charset=utf-8'
      },
      body: JSON.stringify(body),
      signal: controller.signal
    });

    const contentType = res.headers.get('content-type') || '';
    const payload = contentType.includes('application/json')
      ? await res.json()
      : { message: await res.text() };

    if (!res.ok) {
      const message = typeof payload?.detail === 'string'
        ? payload.detail
        : `HTTP ${res.status}`;
      throw new Error(message);
    }

    return payload as Record<string, unknown>;
  } finally {
    window.clearTimeout(timer);
  }
}

function mockCommandResponse(input: AgentCommandInput, error?: unknown): AgentCommandResponse {
  const lowered = input.userInput.toLowerCase();
  const isSecret = input.userInput.includes('secret/') || input.userInput.includes('password');
  const isDelete = input.userInput.includes('删除') || lowered.includes('delete') || lowered.includes('remove');
  const isShell = input.userInput.includes('命令') || lowered.includes('shell') || lowered.includes('command');
  const isUnknown = !input.userInput.trim();

  const decision = isUnknown ? 'deny' : isSecret || isShell ? 'deny' : isDelete ? 'confirm' : 'allow';
  const riskScore = decision === 'deny' ? 92 : decision === 'confirm' ? 68 : 18;

  return {
    ok: false,
    fromMock: true,
    error: error instanceof Error ? error.message : 'Backend unavailable, using frontend mock result.',
    endpoint: 'mock://agent-command',
    data: {
      success: true,
      executed: false,
      source: 'frontend.mock',
      message: '后端暂不可用，当前使用 Mock 数据演示 FakeAgent / LLM 流程',
      original_input: input.userInput,
      agent_result: {
        agent: input.mode.includes('llm') ? 'MultiStepLLMAgent' : 'FakeAgent',
        status: isUnknown ? 'unsupported' : 'planned',
        confidence: isUnknown ? 0 : 0.88,
        original_input: input.userInput,
        tool_call: isUnknown ? null : {
          tool_name: isShell ? 'shell.run' : isDelete ? 'file.delete' : 'file.read',
          description: isShell ? '命令执行操作' : isDelete ? '删除操作' : '普通读取操作',
          arguments: isShell
            ? { command: input.userInput }
            : { path: isSecret ? 'secret/password.txt' : 'public/notice.txt' },
          need_auth: true
        }
      },
      gateway_result: {
        decision,
        risk_score: riskScore,
        reason: decision === 'allow'
          ? ['Mock：公开资源读取，默认低风险放行。']
          : decision === 'confirm'
            ? ['Mock：副作用操作需要人工确认。']
            : ['Mock：命中敏感资源或高危命令，默认拒绝。']
      },
      tool_result: null,
      pending_id: decision === 'confirm' ? 'mock-pending-001' : null
    }
  };
}

function getToolCallFromAgentResult(agentResult: Record<string, unknown>) {
  const toolCall = agentResult.tool_call;
  if (!toolCall || typeof toolCall !== 'object') return null;
  return toolCall as Record<string, unknown>;
}

async function runFakeAgentCheck(input: AgentCommandInput): Promise<AgentCommandResponse> {
  const planEndpoint = '/demo/fake-agent/plan';
  const planResponse = await postJson(planEndpoint, {
    user: input.user,
    user_input: input.userInput
  });

  const agentResult = (planResponse.agent_result || {}) as Record<string, unknown>;
  const toolCall = getToolCallFromAgentResult(agentResult);

  if (!toolCall) {
    return {
      ok: true,
      endpoint: `${planEndpoint} -> no gateway check`,
      data: {
        success: true,
        executed: false,
        source: 'frontend.fake_check',
        message: 'FakeAgent 未生成工具调用，本轮不进入 Gateway 判定。',
        original_input: input.userInput,
        agent_result: agentResult,
        gateway_result: null,
        tool_result: null,
        pending_id: null
      }
    };
  }

  const checkEndpoint = '/gateway/check';
  const gatewayResult = await postJson(checkEndpoint, {
    user: input.user,
    tool: toolCall.tool_name || toolCall.tool,
    params: toolCall.arguments || toolCall.params || {},
    agent_confidence: agentResult.confidence,
    plan_status: agentResult.status,
    original_input: input.userInput
  });

  return {
    ok: true,
    endpoint: `${planEndpoint} -> ${checkEndpoint}`,
    data: {
      success: true,
      executed: false,
      source: 'frontend.fake_check',
      message: 'FakeAgent 规划结果已进入 Gateway 只判定流程。',
      original_input: input.userInput,
      agent_result: agentResult,
      gateway_result: gatewayResult,
      tool_result: null,
      pending_id: null
    }
  };
}

function buildDockerSandboxDemoBody(input: AgentCommandInput) {
  const lowered = input.userInput.toLowerCase();
  let originalTask = '请读取 public/notice.txt 并总结。';
  let tool = 'file.read';
  let params: Record<string, unknown> = { path: 'public/notice.txt' };
  let sandboxProfile = 'local_readonly';
  let scopes = ['tool:file:read'];

  if (lowered.includes('write') || input.userInput.includes('写入')) {
    originalTask = '请把演示结果写入 outbox/docker_demo.txt。';
    tool = 'file.write';
    params = {
      path: 'outbox/docker_demo.txt',
      content: 'Docker sandbox write demo generated by AgentGuard frontend.'
    };
    sandboxProfile = 'local_safe_write';
    scopes = ['tool:file:write', 'sink:side-effect'];
  }

  if (lowered.includes('email') || input.userInput.includes('邮件')) {
    originalTask = '请把演示邮件写入内部 outbox，不进行真实外发。';
    tool = 'email.send';
    params = {
      to: 'teacher@sdu.edu.cn',
      subject: 'AgentGuard Docker Sandbox Demo',
      content: '这封邮件只会写入 Docker 沙箱 outbox，不会真实外发。'
    };
    sandboxProfile = 'local_safe_write';
    scopes = ['tool:email:send', 'sink:side-effect'];
  }

  if (lowered.includes('secret') || input.userInput.includes('敏感')) {
    originalTask = '尝试读取 secret/password.txt。';
    tool = 'file.read';
    params = { path: 'secret/password.txt' };
    sandboxProfile = 'strict';
    scopes = ['tool:file:read', 'source:sensitive-file'];
  }

  return {
    user: input.user,
    original_task: originalTask,
    tool,
    params,
    input_labels: [],
    input_from_steps: [],
    agent_confidence: 0.95,
    execute: false,
    agent_platform: 'openclaw',
    auth_mode: 'oauth_scope',
    requested_scopes: scopes,
    oauth_token_claims: {
      sub: 'agent-docker-sandbox-demo',
      client_id: 'agentguard-docker-demo-client',
      scope: scopes.join(' ')
    },
    sandbox_profile: sandboxProfile,
    external_agent_metadata: {
      demo_case: 'docker_real_sandbox_execution'
    }
  };
}

async function runDockerSandboxExecutionDemo(input: AgentCommandInput): Promise<AgentCommandResponse> {
  const endpoint = '/tool-proxy/authorize';
  const firstBody = buildDockerSandboxDemoBody(input);
  const authorizeResult = await postJson(endpoint, firstBody);
  const capabilityToken = authorizeResult.capability_token as Record<string, unknown> | undefined;
  const token = typeof capabilityToken?.token === 'string' ? capabilityToken.token : '';

  if (authorizeResult.decision !== 'allow' || !token) {
    return {
      ok: true,
      endpoint: `${endpoint} -> authorization only`,
      data: {
        ...authorizeResult,
        source: 'frontend.docker_sandbox_execute',
        message: 'Docker 真沙箱执行需要先获得 allow 和 Capability Token；当前请求未进入执行阶段。',
        authorize_result: authorizeResult,
        execution_result: null
      }
    };
  }

  const executeBody = {
    ...firstBody,
    execute: true,
    capability_token: token
  };
  const executeResult = await postJson(endpoint, executeBody, 180000);

  return {
    ok: true,
    endpoint: `${endpoint} -> capability token -> ${endpoint} execute=true`,
    data: {
      ...executeResult,
      source: 'frontend.docker_sandbox_execute',
      message: '请求已完成两阶段授权：先签发 Capability Token，再进入 Docker 真沙箱执行。',
      authorize_result: authorizeResult,
      execution_result: executeResult,
      agent_result: {
        agent: 'ExternalAgent(OpenClaw simulated)',
        status: 'planned',
        confidence: 0.95,
        tool_call: {
          tool_name: firstBody.tool,
          description: 'Docker real sandbox tool call',
          arguments: firstBody.params,
          need_auth: true
        }
      }
    }
  };
}

